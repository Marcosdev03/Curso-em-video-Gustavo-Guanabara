while True:
    
    num1 = int(input('Digite o primeiro número :'))
    num2 = int(input('Digite o segundo número: '))
    
    if num1 > num2:
        print(f'O {num1} é maior que o {num2}')
    elif num1 < num2:
        print(f'O {num1} é menor que o {num2}')
    else:
        num1 == num2
        print('O dois números são iguais.')